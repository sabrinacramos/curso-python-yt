n1 = int(input('Um valor: '))
n2 = int(input('Um valor: '))

# soma
s = n1 + n2
# subtração
su = n1 - n2
# multiplicação
m = n1 * n2
# divisão
d = n1 / n2
# divisão inteira
di = n1 // n2
# potência
p = n1 ** n2
# resto da divisão
rd = n1 % n2

print(f'A soma é {s}, a subtração é {su}, o produto é {m} e a divisão é {d}')
print(f'Divisão inteira é {di}, potência é {p} e resto da divisão {rd}')